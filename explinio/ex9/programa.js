export function corprimaria(c){

    let corprimaria= '';

    if (c== Amarelo || c==Azul || c== Vermelho){

        corprimaria= 'sim';
    }

    else if (c!= Amarelo || c!=Azul || c!= Vermelho){

        corprimaria= 'não';
    }

    else{

        corprimaria= 'não';
    }

    return corprimaria;
}