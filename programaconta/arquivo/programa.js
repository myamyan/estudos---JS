
export function ovo(opcao){

let ovo ='';


if (opcao == 1)
{
ovo='Ganhe um ovo de pascoa';
}

else if (opcao == 2)
{
ovo='Ganhe um ovinho de pascoa dos ursinhos carinhosos';
}

else if (opcao == 3)
{
ovo='Ganhe um ovo de galinha';
}

else 
{
ovo='Ganhe nada';
}

return ovo;

}