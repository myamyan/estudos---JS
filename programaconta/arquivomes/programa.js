export function mes(data){

    let mes= '';

    if (data==1)
    {

        mes='Janeiro';

    }

    else if (data==2)
    {

        mes='Fevereiro';

    }

    else if (data==3)
    {

        mes='Março';

    }

    else if (data==4)
    {

        mes='Abril';

    }

    else if (data==5)
    {

        mes='Maio';

    }

    else if (data==6)
    {

        mes='Junho';

    }

    else if (data==7)
    {

        mes='Julho';

    }

    else if (data==8)
    {

        mes='Agosto';

    }

    else if (data==9)
    {

        mes='Setembro';

    }

    else if (data==10)
    {

        mes='Outubro';

    }

    else if (data==11)
    {

        mes='Novembro';

    }

    else if (data==12)
    {

        mes='Dezembro';

    }

    else
    {
        mes='Não há mês correspondente'
    }
    
    return mes;

}